const charTable: Map<number, string> = buildTable();

function buildTable(): Map<number, string> {
  const table = new Map<number, string>();

  // 1) Explicit entries (low range, kana, punctuation, emoji, keyboards, extended Latin).
  for (const [key, value] of EXPLICIT_ENTRIES) table.set(key, value);

  // 2) CJK: one character per index starting at CJK_OFFSET.
  for (let i = 0; i < CJK_CHARSET.length; i += 1) {
    table.set(CJK_OFFSET + i, CJK_CHARSET[i]!);
  }

  // 3) ASCII formula fills any holes in 0x21..0x7E at (ascii + 0xFD).
  //    Digits are excluded -- they live at the dedicated 0x0132..0x013B range.
  for (let ascii = 0x21; ascii <= 0x7E; ascii += 1) {
    if (ascii >= 0x30 && ascii <= 0x39) continue;
    const index = ascii + ASCII_OFFSET;
    if (table.has(index)) continue;
    table.set(index, String.fromCharCode(ascii));
  }

  // 4) SPACE + digits at special indices.
  table.set(0x0131, " ");
  for (let digit = 0; digit <= 9; digit += 1) {
    table.set(0x0132 + digit, String.fromCharCode(0x30 + digit));
  }

  return table;
}

/**
 * Decode a Paradise string (little-endian u16 indices) to Unicode.
 * Stops at the first 0x0000 index (null terminator).
 */
export function decodeString(data: Uint8Array, offset = 0): string {
  const view = new DataView(data.buffer, data.byteOffset, data.byteLength);
  let result = "";
  let pos = offset;
  while (pos + 1 < data.byteLength) {
    const index = view.getUint16(pos, true);
    pos += 2;
    if (index === 0x0000) break;
    const glyph = charTable.get(index);
    if (glyph !== undefined) result += glyph;
    // Unknown indices are silently skipped -- matches C# DecodeString behaviour
    // for indices outside any mapped range (not all 65536 slots have glyphs).
  }
  return result;
}

